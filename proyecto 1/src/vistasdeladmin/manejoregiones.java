    private void btneditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditarActionPerformed
   
     String codigoregion = (String) jComboBox1.getSelectedItem();

    int indiceSeleccionado = jComboBox1.getSelectedIndex();

    if (codigoregion.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Por favor, seleccione una región para editar.");
    } else {
        String nuevoNombreRegion = txtregion.getText();
        String nuevoPrecioEstandarString = txtprecioestandar.getText();
        String nuevoPrecioEspecialString = txtprecioespecial.getText();

        if (nuevoNombreRegion.isEmpty() || nuevoPrecioEstandarString.isEmpty() || nuevoPrecioEspecialString.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, llene todos los campos para actualizar la región.");
        } else {
            double nuevoPrecioEstandar = Double.parseDouble(nuevoPrecioEstandarString);
            double nuevoPrecioEspecial = Double.parseDouble(nuevoPrecioEspecialString);

            Region regionSeleccionada = Regiones.getRegiones().get(indiceSeleccionado);
            regionSeleccionada.setNombreRegion(nuevoNombreRegion);
            regionSeleccionada.setPrecioEstandar(nuevoPrecioEstandar);
            regionSeleccionada.setPrecioEspecial(nuevoPrecioEspecial);

            JOptionPane.showMessageDialog(null, "Región actualizada exitosamente.");
                actualizarComboBox();

        }
    }
    
    
    
    
    }//GEN-LAST:event_btneditarActionPerformed

