  private void actualizarComboBox() {
      DefaultComboBoxModel<String> modeloComboBox = (DefaultComboBoxModel<String>) jComboBox1.getModel();
    modeloComboBox.removeAllElements();
    
    Set<String> codigosRegion = new HashSet<>(); 
    
    for (Region region : regiones) {
        codigosRegion.add(region.getCodigoregion());
    }
    
    for (String codigo : codigosRegion) {
        modeloComboBox.addElement(codigo);
    }
    
    jComboBox1.setModel(modeloComboBox);
}
    
    
