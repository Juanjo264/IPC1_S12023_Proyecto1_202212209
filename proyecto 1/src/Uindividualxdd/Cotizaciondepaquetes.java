    private void btncotizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncotizarActionPerformed


  String departamentoSeleccionado = (String) origenedepartamento.getSelectedItem();

int valorSeleccionado = Integer.parseInt(buttonGroup1.getSelection().getActionCommand());

        double precioEstandar = 0.0;
        double precioEspecial = 0.0;
        for (Region region : Regiones.getRegiones()) {
            if (region.getDepartamento().equals(departamentoSeleccionado)) {
                precioEstandar = region.getPrecioEstandar();
                precioEspecial = region.getPrecioEspecial();
                break;
            }
        }

        double precioEstandarTotal = precioEstandar * valorSeleccionado * Double.parseDouble(txtnumeropaquetes.getText());
        double precioEspecialTotal = precioEspecial * valorSeleccionado * Double.parseDouble(txtnumeropaquetes.getText());

        totalestandar.setText(String.valueOf(precioEstandarTotal));
        totalespecial.setText(String.valueOf(precioEspecialTotal));
    



    }//GEN-LAST:event_btncotizarActionPerformed

