    private void descargarcotizacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_descargarcotizacionActionPerformed
     
        
   
       String numeroPaquetes = txtnumeropaquetes.getText();
    String precioEspecial = totalespecial.getText();
    String precioEstandar = totalestandar.getText();
    String tamanoSeleccionado = buttonGroup1.getSelection().getActionCommand();
    String origenSeleccionado = origenedepartamento.getSelectedItem().toString();
    String destinoSeleccionado = destinodepartamento.getSelectedItem().toString();

    // Crear el contenido del archivo HTML
    String contenido = "<html><head><title>Cotización</title></head><body>";
    contenido += "<h1>Cotización</h1>";
    contenido += "<p><strong>Número de Paquetes:</strong> " + numeroPaquetes + "</p>";
    contenido += "<p><strong>Precio Especial:</strong> " + precioEspecial + "</p>";
    contenido += "<p><strong>Precio Estándar:</strong> " + precioEstandar + "</p>";
    contenido += "<p><strong>Tamaño Seleccionado:</strong> " + tamanoSeleccionado + "</p>";
    contenido += "<p><strong>Origen:</strong> " + origenSeleccionado + "</p>";
    contenido += "<p><strong>Destino:</strong> " + destinoSeleccionado + "</p>";
    contenido += "</body></html>";

    // Crear el archivo HTML y guardarlo
    JFileChooser chooser = new JFileChooser();
    chooser.setCurrentDirectory(new File("C:/Users/juanj/OneDrive/Escritorio"));
    chooser.setFileFilter(new FileNameExtensionFilter("Archivos HTML", "html"));
    int resultado = chooser.showSaveDialog(null);
    if (resultado == JFileChooser.APPROVE_OPTION) {
        File archivo = chooser.getSelectedFile();
        String ruta = archivo.getAbsolutePath();
        if (!ruta.endsWith(".html")) {
            ruta += ".html";
        }
        try {
            FileWriter writer = new FileWriter(ruta);
            writer.write(contenido);
            writer.close();
            JOptionPane.showMessageDialog(null, "Archivo guardado exitosamente");
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Error al guardar el archivo");
        }
    }

    
        
    
       }//GEN-LAST:event_descargarcotizacionActionPerformed

