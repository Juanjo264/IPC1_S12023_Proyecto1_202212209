    private void btniniciarsesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btniniciarsesionActionPerformed
              
String correo = txtusuario.getText();
String contrasena = String.valueOf(txtcontrasena.getPassword());

if (correo.equals("ipc1_202212209@ipc1delivery.com") && contrasena.equals("202212209")) {
    Administrador frame = new Administrador();
    frame.setVisible(true);
}else {

boolean usuarioValido = false;
Usuario usuarioEncontrado = null;
for (Usuario u : Usuarios.getListaUsuarios()) {
    if (u.getCorreo().equals(correo) && u.getContrasena().equals(contrasena)) {
        usuarioValido = true;
        usuarioEncontrado = u;
        break;
    }
}



if (usuarioValido) {
    JOptionPane.showMessageDialog(null, "Inicio de sesi칩n exitoso");
    String tipoUsuario = usuarioEncontrado.getTipoUsuario();
    if (tipoUsuario.equals("Kiosco")) {
        Uindividual frame = new Uindividual();
        frame.setVisible(true);
    } else if (tipoUsuario.equals("Usuario individual")) {
        Uindividual frame = new Uindividual();
        frame.setVisible(true);
    }
    
} else if(txtusuario.getText().isEmpty() || txtcontrasena.getText().isEmpty()){
        JOptionPane.showMessageDialog(null, "Ingrese al menos una contrase침a o usuario");

}else {
        JOptionPane.showMessageDialog(null, "Correo o contrase침a incorrectos");

}
}
    }//GEN-LAST:event_btniniciarsesionActionPerformed

