    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String correo = txtcorreos.getText();
        String usuario = txtUsuario.getText();
        String contrasena = txtcontrasena.getText();
        String telefono = txtTelefono.getText();
        String tipoUsuario = btncroles.getSelectedItem().toString();
        String dpi = txtdpi.getText();
        String genero = btncgenero.getSelectedItem().toString();
        String nacionalidad = btncnacionalidad.getSelectedItem().toString();
        Date fechaNacimiento = dateChooser.getDate();
        String tipokiosco = combonuevo.getSelectedItem().toString();


        boolean contieneMayusculas = !contrasena.equals(contrasena.toLowerCase());
        boolean contieneMinusculas = !contrasena.equals(contrasena.toUpperCase());
        boolean contieneNumeros = contrasena.matches(".*\\d.*");
        boolean contieneEspeciales = !contrasena.matches("[A-Za-z0-9]*");
        boolean contrasenaValida = contieneMayusculas && contieneMinusculas && contieneNumeros && contieneEspeciales;

        if (!contrasenaValida) {
            JOptionPane.showMessageDialog(null, "La contraseña debe de estar conformada por:\n" +
"○ Letras mayúsculas\n" +
"○ Letras minúsculas\n" +
"○ Números\n" +
"○ Caracteres especiales (Ej. #,%,&,_,etc.)");
            txtcontrasena.setText("");
            return;
        }

  
        Usuario nuevoUsuario = new Usuario(nombre, apellido, correo, usuario, telefono, dpi, genero, nacionalidad, tipoUsuario, tipokiosco, contrasena, fechaNacimiento);
        
        
        Usuarios.agregarUsuario(nuevoUsuario);
        
        JOptionPane.showMessageDialog(null, "Usuario registrado:\nNombre: " + nombre + 
                 "\nUsuario: " + usuario);
    
    }//GEN-LAST:event_btnRegistrarActionPerformed

