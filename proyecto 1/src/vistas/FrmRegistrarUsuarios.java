    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

       String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String correo = txtcorreos.getText();
        String usuario = txtUsuario.getText();
        String contrasena = txtcontrasena.getText();
        String telefono = txtTelefono.getText();
        String tipoUsuario = btncroles.getSelectedItem().toString();
        String dpi = txtdpi.getText();
        String genero = btncgenero.getSelectedItem().toString();
        String nacionalidad = btncnacionalidad.getSelectedItem().toString();
        Date fechaNacimiento = dateChooser.getDate();
        String tipokiosco = combonuevo.getSelectedItem().toString();
    
    Usuario usuarioModificado = new Usuario(nombre, apellido, correo, usuario, telefono, dpi, genero, nacionalidad, tipoUsuario, tipokiosco, contrasena, fechaNacimiento);
    
    boolean usuarioEncontrado = false;
    for (int i = 0; i < Usuarios.getListaUsuarios().size(); i++) {
        Usuario u = Usuarios.getListaUsuarios().get(i);
        if (u.getCorreo().equals(correo)) {
            Usuarios.getListaUsuarios().set(i, usuarioModificado);
            usuarioEncontrado = true;
            break;
        }
    }
    
    if (usuarioEncontrado) {
        JOptionPane.showMessageDialog(null, "Usuario modificado correctamente");
    } else {
        JOptionPane.showMessageDialog(null, "El usuario no existe ");
    }
    }//GEN-LAST:event_btnModificarActionPerformed

